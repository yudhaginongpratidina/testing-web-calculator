const Calculator = {
    penjumlahan(firstNumber, secondNumber) {
        return firstNumber + secondNumber;
    },
    pengurangan(firstNumber, secondNumber) {
        return firstNumber - secondNumber;
    },
    perkalian(firstNumber, secondNumber) {
        return firstNumber * secondNumber;
    },
    pembagian(firstNumber, secondNumber) {
        return firstNumber / secondNumber;
    }
}

export default Calculator;